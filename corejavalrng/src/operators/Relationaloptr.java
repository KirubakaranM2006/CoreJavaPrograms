package operators;

public class Relationaloptr {

	public static void main(String[] args) {
		
		int x=5;
		int y=10;
		System.out.println(x<y);//xas5 yas10 so 5<10 which is (true)
		System.out.println(x>y);//xas5 yas10 so 5>10 which is (false)
		

	}

}
//_____________________________________________________________________________________________
//Operator   |	Meaning	Example (x = 10, y = 5)	               Result
//____________________________________________________________________________________________
//     ==	 |                       Equal to	                          x == y  | false
//     !=	 |                      Not equal to	                      x != y  |true
//     >	 |                      Greater than	                      x > y	  |true
//     <	 |                     Less than	                          x < y	  |false
//     >=	 |                    Greater than or equal to	              x >= y  |true
//     <=	 |                   Less than or equal to	                  x <= y  |false
