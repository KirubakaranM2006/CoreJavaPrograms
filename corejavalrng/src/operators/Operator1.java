package operators;

public class Operator1 {

	public static void main(String[] args) {
		
		
		//addition(+)
		int a=10;
		int b=10;
		int c=a+b;
		System.out.println("the addition is:"+c);//1st variable + 2nd variable
		
		//output: 20
		
		//subraction(-)
		int a1,b2,c1;
		a1=10;
		b2=10;
		c1=a1-b2;
		System.out.println("the subraction is:"+c1);
		//1st variable - 2nd variable
		//output:0
		
		//multiplication(*)
		int a2=4;
		int b1=10;
		System.out.println("the multiplication of this:"+a2*b1);//a2 multiplies with b1 (output:40)
		
		//division(/)
		int q1=10;
		int q2=2;
		System.out.println("division is:"+q1/q2);// divides the first operand by 2nd operand
		
		//modulus(%) or remainder operator
		int w1=20;
		int w2=3;
		System.out.println("modulus is:"+w1%w2);
		
		
	}

}
