//instanceof is used to check if an object belongs to a specific type (class).
//It returns:
//true if the object is of the specified type.
//false if it is not.

                          //syntax:object instanceof ClassName
                       

package operators;

public class Instanceof_optr {

	public static void main(String[] args) {
		
		        String str = "Hello, World!";
		        Integer num = 100;

		        // Check if str is an instance of String
		        System.out.println(str instanceof String);  // true

		        // Check if num is an instance of Integer
		        System.out.println(num instanceof Integer); // true

		       // here while using instance of string and int are not primitive datatype
		    }
		}

		

