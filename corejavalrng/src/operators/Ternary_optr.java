package operators;

public class Ternary_optr {

	public static void main(String[] args) {
               //The ternary operator is a shortcut for if-else statements. It takes three operands:
		        int a = 10, b = 20;
		        
		        // Using ternary operator to find the maximum
		        int max = (a > b) ? a : b;
		        
		        System.out.println("Maximum is: " + max);
		    }
		}



//(a > b) is the condition.
//If true, a is returned.
//If false, b is returned.

