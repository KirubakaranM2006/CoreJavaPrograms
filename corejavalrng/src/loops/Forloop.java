//used when you know how many times the loop wants to run

package loops;

public class Forloop {

	public static void main(String[] args) {
		//here i=1 is initial value
		//i<=10 is condition
		//i++ is increment step
		for(int i=1;i<=10;i++) {
			System.out.println("*");
		}

	}

}
