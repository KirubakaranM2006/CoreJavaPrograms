package condtnstatements;

public class If {

	public static void main(String[] args) {
		int k=10;
		if(k>5) {
			System.out.println(+k);//if the if condition is true it returns the actual condition if it false go to else part
		}
		else {
			System.out.println("lesser");//if the if condition is false it comes to else part
		}
	
	}

}
