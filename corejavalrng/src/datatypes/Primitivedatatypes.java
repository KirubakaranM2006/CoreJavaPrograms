package datatypes;

public class Primitivedatatypes {

	public static void main(String[] args) {
		
		byte no=97; //byte only have range from (-128->127)
		System.out.println("ByteValue:"+no);
		
		short no1=30000;              //range (-32k to 32k)
		System.out.println("Shortvalue:"+no1);
		
		int n=1000000;// range (-2billionto2billion)
		System.out.println("Int value:"+n);
		
		long n1=10000000000000l;//last added l is must for defining long datatype (range above 2billion)
		System.out.println("LOng value:"+n1);
		
		
		float n2=521.7f;// used to print decimal point value 
		System.out.println("float value:"+n2);
		
		
		double n3=1.99787774;// use this for more dicimal digits
		System.out.println("double value:"+n3);
		
		char n4='L'; //used to stored a single character
		System.out.println("Char value:"+n4);
		
		boolean a=true;// used to print true or false
		System.out.println("boolean value:"+a);
		
		
		
		
		
	}

}
